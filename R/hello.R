#
# Description
#' Say hello to someone as many times as you wish
#'
# Arguments
#' @param sayTo - The name you want to say hello to.
#' @param num - Number to repeat hello.
#'
# Returns
#' @return Prints hello statements to console


hello <- function(sayTo = "world", num = 1) {
  print(rep(sprintf("Hello %s",sayTo),num))
}
